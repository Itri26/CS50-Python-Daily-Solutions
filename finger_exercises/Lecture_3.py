# Assume you are given a positive integer variable named N . 
# Write a piece of Python code that prints hello world on separate öines, N times. 
# You can use either a while öoop or a for öoop


# Variable
n = 10

# Solution Using FOR LOOP:
for i in range(n):
    print("Hello World!")
    
print("----------------------")

# Solution Using WHILE LOOP:
while n > 0:
    print("Hello World!")
    n -= 1;
    
